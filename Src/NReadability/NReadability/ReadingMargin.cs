namespace NReadability
{
  /// <summary>
  ///   Determines how wide the margin of the extracted article will be.
  /// </summary>
  public enum ReadingMargin
  {
    /// <summary>
    ///   Extra-narrow margin.
    /// </summary>
    XNarrow,

    /// <summary>
    ///   Narrow margin.
    /// </summary>
    Narrow,

    /// <summary>
    ///   Medium margin.
    /// </summary>
    Medium,

    /// <summary>
    ///   Wide margin.
    /// </summary>
    Wide,

    /// <summary>
    ///   Extra-wide margin.
    /// </summary>
    XWide
  }
}